package com.aerilys.ageoftrinity.shared;

import java.io.Serializable;

public class PersoEntity implements Serializable
{
  public String user;
  String nom;
  int indexRace;
  int indexClasse;
  int indexDestinee;
  String biographie;
  int niveau;
  
  public PersoEntity()
  {
    
  }
  

  /**
   * @return the nom
   */
  public String getNom()
  {
    return nom;
  }

  /**
   * @param nom the nom to set
   */
  public void setNom(String nom)
  {
    this.nom = nom;
  }

  /**
   * @return the biographie
   */
  public String getBiographie()
  {
    return biographie;
  }

  /**
   * @param biographie the biographie to set
   */
  public void setBiographie(String biographie)
  {
    this.biographie = biographie;
  }

  /**
   * @return the niveau
   */
  public int getNiveau()
  {
    return niveau;
  }

  /**
   * @param niveau the niveau to set
   */
  public void setNiveau(int niveau)
  {
    this.niveau = niveau;
  }

  /**
   * @return the indexRace
   */
  public int getIndexRace()
  {
    return indexRace;
  }

  /**
   * @param indexRace the indexRace to set
   */
  public void setIndexRace(int indexRace)
  {
    this.indexRace = indexRace;
  }

  /**
   * @return the indexClasse
   */
  public int getIndexClasse()
  {
    return indexClasse;
  }

  /**
   * @param indexClasse the indexClasse to set
   */
  public void setIndexClasse(int indexClasse)
  {
    this.indexClasse = indexClasse;
  }

  /**
   * @return the indexDestinee
   */
  public int getIndexDestinee()
  {
    return indexDestinee;
  }

  /**
   * @param indexDestinee the indexDestinee to set
   */
  public void setIndexDestinee(int indexDestinee)
  {
    this.indexDestinee = indexDestinee;
  }
}
