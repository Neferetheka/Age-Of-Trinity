package com.aerilys.ageoftrinity.client.html4gwt;


public class inputMail extends RequiredTextBox{
  
  boolean required = false;
  
  
  
  public inputMail()
  {
    this.getElement().removeAttribute("type");
    this.getElement().setAttribute("type", "email");
    this.setStyleName("gwt-TextBox-perso");
    this.setMaxLength(200);
  }
}
