package com.aerilys.ageoftrinity.client.models;

public class Destinee {

  int id;
  String nom;
  String description;
  String effet;
  String image;
  int prix;
  
  public Destinee(int id, String nom, String description, String effet, String image, int prix)
  {
    super();
    this.id = id;
    this.nom = nom;
    this.description = description;
    this.effet = effet;
    this.image = image;
    this.prix = prix;
  }
 

  /**
   * @return the id
   */
  public int getId() {
    return id;
  }
  /**
   * @param id the id to set
   */
  public void setId(int id) {
    this.id = id;
  }
  /**
   * @return the nom
   */
  public String getNom() {
    return nom;
  }
  /**
   * @param nom the nom to set
   */
  public void setNom(String nom) {
    this.nom = nom;
  }
  /**
   * @return the image
   */
  public String getImage() {
    return image;
  }
  /**
   * @param image the image to set
   */
  public void setImage(String image) {
    this.image = image;
  }
  /**
   * @return the description
   */
  public String getDescription() {
    return description;
  }
  /**
   * @param description the description to set
   */
  public void setDescription(String description) {
    this.description = description;
  }
  /**
   * @return the effet
   */
  public String getEffet() {
    return effet;
  }
  /**
   * @param effet the effet to set
   */
  public void setEffet(String effet) {
    this.effet = effet;
  }
  /**
   * @return the prix
   */
  public int getPrix() {
    return prix;
  }
  /**
   * @param prix the prix to set
   */
  public void setPrix(int prix) {
    this.prix = prix;
  }
  
  
  
  
  
}
